#include <stdio.h>

int main() {
    double t1, t2, t3;
    printf("Введіть час в годинах, за який кожен гість може з'їсти пиріг (t1, t2, t3): ");
    scanf("%lf %lf %lf", &t1, &t2, &t3);

    // Розрахунок загальної швидкості з'їдання пирога
    double total_time = 1 / (1 / t1 + 1 / t2 + 1 / t3);

    // Виведення результату з точністю до двох знаків після коми
    printf("Час, за який гості разом можуть з'їсти пиріг: %.2f годин\n", total_time);

    return 0;
}